package com.company;

public class Encapsulation {

    private String myName;
    private int myAge;




    public void setMyName(String myName) {
        this.myName = myName;
    }

    public String getMyName() {
        return myName;
    }

    public void setMyAge(int myAge) {
        this.myAge = myAge;
    }

    public int getMyAge() {
        return myAge;
    }
}
